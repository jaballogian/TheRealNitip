public class BarangInHome {

    String namaBarang, hargaBarang, jenisNitip, namaPemilik, tanggal, asalKota;
    int fotoBarang, logoJenisNitip;

    public BarangInHome(){

    }

    public String getNamaBarang(){
        return namaBarang;
    }

    public void setNamaBarang(String namaBarang) {
        this.namaBarang = namaBarang;
    }

    public String getHargaBarang() {
        return hargaBarang;
    }

    public void setHargaBarang(String hargaBarang) {
        this.hargaBarang = hargaBarang;
    }

    public String getJenisNitip() {
        return jenisNitip;
    }

    public void setJenisNitip(String jenisNitip) {
        this.jenisNitip = jenisNitip;
    }

    public String getNamaPemilik() {
        return namaPemilik;
    }

    public void setNamaPemilik(String namaPemilik) {
        this.namaPemilik = namaPemilik;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    public String getAsalKota() {
        return asalKota;
    }

    public void setAsalKota(String asalKota) {
        this.asalKota = asalKota;
    }


    public int getFotoBarang() {
        return fotoBarang;
    }

    public void setFotoBarang(int fotoBarang) {
        this.fotoBarang = fotoBarang;
    }

    public int getLogoJenisNitip() {
        return logoJenisNitip;
    }

    public void setLogoJenisNitip(int logoJenisNitip) {
        this.logoJenisNitip = logoJenisNitip;
    }

}
